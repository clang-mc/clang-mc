def render_enum(enum_spec: dict) -> str:
    enum_values = enum_spec["values"]
    if not enum_values:
        raise ValueError(
            f"enum {enum_spec.get('name', '<unnamed>')!r} has no values; "
            "cannot generate an empty enum body"
        )
    values = ",\n    ".join(value["const"] for value in enum_values)
    return f"""typedef enum {{
    {values}
}} {enum_spec["name"]};
"""


def command_c_name(command_name: str) -> str:
    return re.sub(r"[^0-9A-Za-z_]", "_", command_name)


